package JavaPrograms;

public class String_toUpperase_toLowerCase {

	public static void main(String[] args) {
		//This method is used to convert the String to upper or lower case characters
		
		String s1 = "hello";
		String s2 = "WORLD";
		
		System.out.println(s1.toUpperCase());
		
		System.out.println(s2.toLowerCase());

	}

}
