package JavaPrograms;

public class Switch_Case {

	public static void main(String[] args) 
	{
		//Print Weekdays using Switch case
		
		int num = 20;
		
		switch(num)
		{
		case 01: System.out.println("Monday");
		break;
		
		case 02: System.out.println("Tueday");
        break;
        
		case 03: System.out.println("Wednesday");
        break;
        
		case 04: System.out.println("Thursday");
        break;
        
		case 05: System.out.println("Friday");
        break;
        
		case 06: System.out.println("Saturday");
        break;
        
		case 07: System.out.println("Sunday");
        break;
        
		default: System.out.println("Wrong Input");
        
		}

	}

}
